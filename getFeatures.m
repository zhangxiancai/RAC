function  D=getFeatures(D1)
%得到D1中每个列向量的均值，方差，最大值，最小值，中位数信息
%D1=3维矩阵，第三维是轴方向，一二维构成的矩阵的每一列是每个采样点特定邻域内数据，
%D=3维矩阵，每一列记录相应的向量的特征信息
[~,s,~]=size(D1);
D=zeros(5,s,3);
for k=1:3
    D(1,:,k)=mean(D1(:,:,k));
    D(2,:,k)=var(D1(:,:,k));
    D(3,:,k)=max(D1(:,:,k));
    D(4,:,k)=min(D1(:,:,k));
    D(5,:,k)=median(D1(:,:,k));
end

end